  /// Look up translated string for [key] using locale from [context]. Falls back to English then key.
  static String tr(BuildContext context, String key) {
    final notifier = context.read<LocaleNotifier>();
    final tag = notifier.languageTag;
    return _translations[tag]?[key] ?? _translations['en']?[key] ?? key;
  }
